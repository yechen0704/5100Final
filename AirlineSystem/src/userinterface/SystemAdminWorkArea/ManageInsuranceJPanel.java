    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDm.getSelectedRow();
        if(selectedRow>=0){

            String username= (String) tblDm.getValueAt(selectedRow, 0);
            String pwd= (String) tblDm.getValueAt(selectedRow, 1);
            UserAccount user=system.getUserAccountDirectory().authenticateUser(username, pwd);

            //UserAccount user = (UserAccount) networkJTable.getValueAt(selectedRow, 0);
            system.getUserAccountDirectory().deleteUserAccount(user);
            system.getDeliveryManDirectory().deleteDeliveryMan(user.getUsername());

            populatetblDm();
        }else{
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

